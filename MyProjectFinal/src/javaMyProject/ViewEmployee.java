    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
        try {
            String u = jTextFieldID.getText();
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/swing", "root", "1234");
            String sql = "select * from employee4 where  emp_id='" + u + "'";
            Statement st = con.createStatement();
            //pst.setString(1, u);

            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                jLabelName.setText(rs.getString("name"));
                jLabelFName.setText(rs.getString("fname"));
                jLabelDepartment.setText(rs.getString("department"));
                jLabelPost.setText(rs.getString("post"));
                jLabelAge.setText(rs.getString("age"));
                jLabelGender.setText(rs.getString("gender"));
                jLabelBirth.setText(rs.getString("date_of_birth"));
                jLabelSalary.setText(rs.getString("salary"));
                jLabelPhone.setText(rs.getString("phone"));
                jLabelEmail.setText(rs.getString("email"));
                jLabelEducation.setText(rs.getString("education"));
                jLabelAddress.setText(rs.getString("address"));

                byte[] img = rs.getBytes("image");

                //Resize The ImageIcon
                ImageIcon image = new ImageIcon(img);
                Image im = image.getImage();
                Image myImg = im.getScaledInstance(jLabelPic.getWidth(), jLabelPic.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon newImage = new ImageIcon(myImg);
                jLabelPic.setIcon(newImage);

                st.close();
                con.close();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Name and Password does not match !");

            }

        } catch (Exception ex) {
            //Logger.getLogger(RemoveEmployee.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("The error is:" + ex);
        }
    }//GEN-LAST:event_jButtonSearchActionPerformed


    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        // TODO add your handling code here:
        int p = JOptionPane.showConfirmDialog(null, "Do you want to delete all records?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if (p == 0) {
            try {

                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/swing", "root", "1234");
                String sql = "truncate table loginIn";
                Statement st = con.createStatement();
                st.executeUpdate(sql);

                JOptionPane.showMessageDialog(rootPane, "Data Deleted");

                con.close();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e);
            }
        }
    }//GEN-LAST:event_jLabel5MouseClicked


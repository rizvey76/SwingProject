    private void jLabelSignInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelSignInMouseClicked
        // TODO add your handling code here:
        try {
            String u = jTextUsername.getText();
            String v = jPasswordField.getText();
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/swing", "root", "1234");
            String sql = "select * from loginIn where username=? and password=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, u);
            pst.setString(2, v);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                HomePage hom = new HomePage();
                hom.setVisible(true);
                setVisible(false);

            } else {
                JOptionPane.showMessageDialog(rootPane, "Name and Password does not match !");
                jTextUsername.setText(null);
                jPasswordField.setText(null);

            }

            con.close();

        } catch (Exception e) {
            Logger.getLogger(Log_In.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_jLabelSignInMouseClicked


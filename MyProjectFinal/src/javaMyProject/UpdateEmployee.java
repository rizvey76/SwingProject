    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
        try {
            String u = jTextFieldID.getText();
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/swing", "root", "1234");
            String sql = "select * from employee4 where  emp_id='" + u + "'";
            Statement st = con.createStatement();
            //pst.setString(1, u);

            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                jTextName.setText(rs.getString("name"));
                jTextFname.setText(rs.getString("fname"));
                jTextDepartment.setText(rs.getString("department"));
                jTextPost.setText(rs.getString("post"));
                jTextAge.setText(rs.getString("age"));
                jTextFieldBir.setText(rs.getString("date_of_birth"));
                jTextSalary.setText(rs.getString("salary"));
                jTextPhone.setText(rs.getString("phone"));
                jTextEmail.setText(rs.getString("email"));
                jTextEducation.setText(rs.getString("education"));
                jTextAddress.setText(rs.getString("address"));

                byte[] img = rs.getBytes("image");

                //Resize The ImageIcon
                ImageIcon image = new ImageIcon(img);
                Image im = image.getImage();
                Image myImg = im.getScaledInstance(jLabelPic.getWidth(), jLabelPic.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon newImage = new ImageIcon(myImg);
                jLabelPic.setIcon(newImage);

                st.close();
                con.close();
            } else {
                JOptionPane.showMessageDialog(rootPane, "ID does not match !");

            }

        } catch (Exception ex) {
            //Logger.getLogger(RemoveEmployee.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("The error is:" + ex);
        }
    }//GEN-LAST:event_jButtonSearchActionPerformed

